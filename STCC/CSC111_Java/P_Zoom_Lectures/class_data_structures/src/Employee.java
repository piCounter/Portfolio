public class Employee {
    int EmpNum;
    String EmpLname;
    String EmpFname;
    double EmpSalary;
    
    public int getEmpNum() {
        return EmpNum;
    }

    public void setEmpNum(int Num) {
        EmpNum = Num;
    }

    public String getEmpLname(String Lname) {
        return EmpLname;
    }

    public void setEmpLname(String Lname) {
        EmpLname = Lname;
    }

    public String getEmpFname(String Fname) {
        return EmpFname;
    }

    public void setEmpFname(String Fname) {
        EmpFname = Fname;
    }

    public double getEmpSalary(String Salary) {
        return EmpSalary;
    }

    public void setEmpSalary(double Salary) {
        EmpSalary = Salary;
    }

}
